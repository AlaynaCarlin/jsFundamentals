//Assignment Operator
let x = 20; 

//addition assignment operator
x += 1;// x = x+1
console.log(x);

//subtraction assignment operator
x -= 1;//x = x - 1
console.log(x);

//multiplication assignment operator
x *= 2;//x = x * 2
console.log(x);

//division assignment operator
x /= 4;//x = x / 4
console.log(x);

// remaonder/modulus assignment operator
x %= 2;//x = x % 2 -> no remainder
console.log(x);

//modulus operator
console.log(5%3);// 5 / 3 = 1 2/3 ->2
console.log(25%7);//remainder

// exponential assignment operator
x **=2;//0

let w = 7;
w **= 3;
console.log(w);



